							<h1>Download Reports</h1><br>
							<b>Description:</b> This tool allows the user to download a log file from the report of their choosing. Depending on the selected report, the download options will vary. <br>Some report types consist of various subreports, and some reports are stored differently (hourly or daily), so the options will vary accordingly. <br><br>
							<b>Instructions:</b>
							<ul>
							<li>Select the report type that you wish to download</li>
							<li>If there are any available subreports for the type that you selected, then you must select that subreport type</li>
							<li>Once the report has been fully selected (no more types to select), select the date of the report that you wish to access</li>
							<li>Once you have selected all of the above, the 'Download' button will now be accessible, as long as that the date option is currently highlighted</li>
              <li>Click the 'Download' button to download your selected report</li>
<br>
		</ul>

							<b>Download issues:</b>
							<ul>
							<li class="indent">If, for whatever reason, the 'Download' button does not download anything after you have selected all the required options, please reload the page by clicking on the Home navigation (logo or through the menu) or pressing F5.</li>
							<li>After reloading the page, select the required options for your desired report and press the 'Download' button once again.</li>
							<li>If using Internet Explorer, right-click on the Download button and select "Save target as..." to download the file</li>
							</ul>
							<br>
              <b>Incorrect downloads:</b>
		<ul>					
		<li>In the event that a report was successfully downloaded, but it was not the correct report that was expected, please first check if the report is correctly stored on the WBSS server.</li>
              <li>If the report looks correct and as expected on the server, then it is most likely a website issue (incorrect file linking). Report the issue.</li>
		</ul>
